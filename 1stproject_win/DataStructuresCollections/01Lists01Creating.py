# Collection problems
# LISTS 1
# Creating

# Lists 1 -> Empty Lists
x = []
print(x)
print(type(x))

# Lists 2 -> Separating with commas
y = [10, 20, 30, 40]
print(y)
print(type(y))

# Lists 3 -> Heterogeneous
z = [101, 'naresh', 'python', 400.01]
print(z)
print(type(z))

# Lists 4 -> Using list(iterable) constructor/function
list1 = list(range(1, 11))
print(list1)
print(type(list1))

list2 = list("PYTHON")
print(list2)
print(type(list2))

list3 = list(range(10, 0, -1))
print(list3)
print(type(list3))

list4 = list([10, 20, 30, 40, 50])
print(list4)
print(type(list4))

list5 = list()
print(list5)
print(type(list5))

