# '+' - to concatenate/join lists

l1 = [10, 20, 30]
l2 = [40, 50, 60]
l3 = l1 + l2
print(l1, l2, l3, sep="\n")
print()

# '*' - to repeat elements n number of times

print()
l4 = l1 * 4
print(l4)
print()

# '==' to compare list elements, but not id

print()
print(l1 == l2)
print()
