# Example 1:
employee_data = {101: {'emp_name': 'naresh', 'job': 'CEO', 'salary': 200000},
                 102: {'emp_name': 'suresh', 'job': 'Manager', 'salary': 40000}}
print()
print(employee_data)
print()
print(employee_data[101])
print()
print(employee_data[102]['job'])
print()

